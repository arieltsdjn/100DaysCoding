public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        // operasi modulus
        int hasil = a % b;

        // output
        System.out.println(a + " % " + b + " = " + hasil);

        // contoh lain
        System.out.println("15 % 4 = " + (15 % 4));
        System.out.println("8 % 2 = " + (8 % 2));
    }
}