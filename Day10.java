public class Main {
    public static void main(String[] args) {

        final byte umur = 20;
        final short tinggiBadan = 170;
        final int uangSaku = 50000;
        final long nomorInduk = 1234567890L;
        final float beratBadan = 55.5f;
        final double pi = 3.14159;
        final char nilaiUjian = 'B';
        final boolean lagiLapar = true;

        System.out.println("Umur (byte): " + umur);
        System.out.println("Tinggi badan (short): " + tinggiBadan);
        System.out.println("Uang saku (int): " + uangSaku);
        System.out.println("Nomor induk (long): " + nomorInduk);
        System.out.println("Berat badan (float): " + beratBadan);
        System.out.println("Pi (double): " + pi);
        System.out.println("Nilai ujian (char): " + nilaiUjian);
        System.out.println("Lagi lapar? (boolean): " + lagiLapar);
    }
}